void initializeAxolotls() {
    for (int i = 0; i < AXOLOTLCOUNT; i++) {
        axolotls[i].bitmap = axolotlBitmaps[i];
        switch (i) {
            case 0:
                axolotls[i].color = LIGHTPINK;
                break;
            case 1:
                axolotls[i].color = LIGHTBLUE;
                break;
            case 2:
                axolotls[i].color = LIGHTGREEN;
                break;
            case 3:
                axolotls[i].color = LIGHTYELLOW;
                break;
            case 4:
                axolotls[i].color = LIGHTPURPLE;
                break; 
        }
    }
}

void drawAxolotl(AXOLOTL* axolotl_ptr, int index) {
    int y = SCREENHEIGHT / 2 - 16;
    int x = 20 + (index * 45);
    for (int i = 0; i < AXOLOTLLEN; i++) {
        for (int j = 0; j < AXOLOTLLEN; j++) {
            // retrieve the value of the axolotlBitmap at a given location
            int value = axolotl_ptr->bitmap[OFFSET(i, j, AXOLOTLLEN)];
            // TODO 3.1: Complete the switch statement; refer to the lab PDF for case by case instructions
            switch (value) {
                case 1:
                    setPixel(x + i, y + j, BLACK);
                    break;
                case 2:
                    setPixel(x + i, y + j, RED);
                    break;
                case 3:
                    setPixel(x + i, y + j, MAGENTA);
                    break;
                case 4:
                    setPixel(x + i, y + j, axolotl_ptr -> color);
                    break;
                case 5:
                    setPixel(x + i, y + j, DARKGRAY);
                    break;
                
                default:
                    break;
            }
        }
    }
}